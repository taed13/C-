#include <iostream>
using namespace std;

int main()
{
    // Bài 1: Nhập vào 2 số a và b. Bạn hãy in ra màn hình tổng và hiệu của 2 số đó.
    int a, b;
    cin >> a;
    cin >> b;
    cout << "a "
         << "+"
         << " b = " << a + b << endl;

    cout << "a "
         << "-"
         << " b = " << a - b << endl;
    return 0;
}
